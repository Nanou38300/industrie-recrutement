    <article id="article-chimie">
        <h2>LE SECTEUR CHIMIE</h2>
        <p>Le secteur de la chimie est un pilier fondamental de l’industrie mondiale. Il regroupe l’ensemble des activités liées à la transformation de la matière première (pétrole, gaz naturel, minéraux, biomasse, etc.) en produits utiles à d'autres secteurs industriels ou à la consommation quotidienne.</p>

        <p>La chimie est omniprésente dans notre vie quotidienne : elle intervient dans la fabrication de produits pharmaceutiques, cosmétiques, plastiques, peintures, détergents, engrais, textiles, matériaux de construction et bien d'autres.</p>

        <p>Une industrie stratégique et innovante</p>
        <p>Le secteur de la chimie est divisé en plusieurs grandes branches :</p>

        <p>- La chimie de base : production de matières premières chimiques en grande quantité (acide sulfurique, ammoniac, éthylène, etc.).</p>

        <p>- La chimie fine : fabrication de molécules complexes à haute valeur ajoutée, souvent destinées à l'industrie pharmaceutique ou agrochimique.</p>

        <p>- La chimie de spécialité : produits conçus pour des usages spécifiques (adhésifs, encres, additifs alimentaires...).</p>

        <p>Ce secteur est également un moteur d'innovation. Il investit massivement dans la recherche et développement (R&D) pour créer des matériaux plus performants, des procédés moins polluants, ou encore des alternatives durables aux produits issus du pétrole.</p>

        <p>Un rôle clé dans les transitions écologique et énergétique</p>
        <p>Face aux enjeux environnementaux actuels, la chimie joue un rôle essentiel dans la transition vers une industrie plus durable. Cela passe par le développement de la chimie verte, l’éco-conception, le recyclage des matériaux, ou encore l’utilisation de ressources renouvelables.</p>

        <p>Poids économique
        <p>La chimie est l’un des secteurs industriels les plus puissants au monde. En Europe, elle représente un chiffre d'affaires de plusieurs centaines de milliards d’euros et emploie des millions de personnes, directement ou indirectement.</p>

        <p>En résumé, la chimie est un secteur à la fois stratégique, innovant et en pleine mutation, qui joue un rôle clé dans de nombreux domaines économiques et dans la construction d’un avenir plus durable.</p>
        
