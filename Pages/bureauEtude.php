        <p>La filière nucléaire française est aujourd’hui confrontée à plusieurs défis majeurs :</p>

        <p>Le prolongement de la durée de vie des centrales existantes (projet « Grand Carénage »).</p>

        <p>La construction de nouveaux réacteurs (EPR2).</p>

