        <h2>LE SECTEUR NUCLÉAIRE</h2>

        <p>La France est l’un des pays les plus avancés au monde dans le domaine de l’énergie nucléaire. Ce secteur joue un rôle stratégique dans la production d’électricité nationale : environ 70 % de l’électricité produite en France provient du parc nucléaire, qui compte 56 réacteurs répartis sur 18 sites (source : EDF, 2024).
        <p>Le nucléaire présente plusieurs atouts :Il assure une production d’énergie continue (non soumise aux aléas climatiques, comme l’éolien ou le solaire).
        <p>Il contribue à la réduction des émissions de CO₂, car il ne rejette pas de gaz à effet de serre lors de la production d’électricité.
        <p>Il renforce l’indépendance énergétique de la France vis-à-vis des importations d’énergies fossiles.
        <p>La maintenance nucléaire : un enjeu majeur
        C'est une activité essentielle pour garantir la sécurité, la fiabilité et la longévité des installations nucléaires. Elle regroupe l’ensemble des opérations techniques nécessaires pour assurer le bon fonctionnement des réacteurs et équipements associés, tout en respectant des normes de sûreté très strictes.


        <ul class="be-nucleaire">Il existe plusieurs types de maintenance dans le nucléaire :
        <li>Maintenance préventive : réalisée régulièrement pour éviter les pannes (ex : contrôle des circuits, nettoyage, remplacement de pièces).
        <li>Maintenance corrective : en cas de dysfonctionnement ou d’arrêt non planifié.
        <li>Maintenance prédictive : basée sur des données (capteurs, IA) pour anticiper les défaillances.
        <li>Grands Arrêts (GA) : interventions programmées tous les 10 à 18 mois pour recharger le combustible et effectuer des vérifications approfondies. Ces opérations mobilisent plusieurs centaines de techniciens spécialisés.
